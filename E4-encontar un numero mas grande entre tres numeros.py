#solicita al usuario que ingrse tres numeros.

a = float(input("Ingrese el primer numero: "))

b = float(input("Ingrese el segundo numero: "))

c = float(input("Ingrese el tercer numero: "))

#encuentra el numero mas grande utilizando la funcion max()

max_num = max(a, b, c)

#muestra el numero mas grande encontrado

print("El numero mas grande es:", max_num)

