#solicita al usuario que ingrese una temperatura en grados Celsius.

Celsius = float(input("Ingrese la temperatura en grados Celsius: "))

#convierte la temperatura a grados Fahrenheit utilizando la formula correspondiente.

Fahrenheit = (Celsius * 9/5) + 32

#muestra la temperatura convertida en grados fahrenheit.

print("La temperatura en grados Fahrenheit es:", Fahrenheit)




