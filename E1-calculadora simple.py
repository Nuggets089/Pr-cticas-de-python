#solicita al usuario que ingrese dos numeros enteros

a = int(input("ingrese el primer numero"))

b = int(input("ingrese el segundo numero"))


#muestra la suma, la diferencia, el producto y el cociente ingresados

print("suma" , a + b) 

print("diferencia" , a - b)

print("producto" , a * b)

print("cociente" , a / b)  #Observacion: Esto puede devolver un valor decimal incluso si los numeros ingresados son enteros.
